    const admindAccount = web3.eth.accounts.privateKeyToAccount(process.env["MAINNET_PRIVATE_KEY"]);
    web3.eth.defaultAccount = admindAccount.address;
    const NFTAddress = '0x23ff55bccfb446864814d19eddd11fb42615d50a';
    const contract = new web3.eth.Contract(contractABICANARY, NFTAddress);

    var index = 400;
    var add = 100;
    var end = 3040;
    var toIndex = index;

    var stop = false;

    while (stop == false) {

        toIndex = toIndex+add;

        if (toIndex > end) {
            toIndex = end;
            stop = true;
        }

        let mintArr = mintArray.slice(index, toIndex);

        const tx = contract.methods.mintMany(
            index,
            mintArr,
        )

        console.log(" ");
        console.log("HOW LONG IT IS: ", mintArr.length);
        console.log("ESTIMATED GAS: ", await tx.estimateGas({from: admindAccount.address}));

        // console.log("DO THEY MATCH? ", contract.options.address, " = ", NFTAddress);

        const signedTx = await admindAccount.signTransaction({
            to: contract.options.address,
            data: tx.encodeABI(),
            gasPrice: web3.utils.toHex(await web3.eth.getGasPrice()),
            gasLimit: web3.utils.toHex(await await tx.estimateGas({from: admindAccount.address})),
        });

        const response = await web3.eth.sendSignedTransaction(signedTx.rawTransaction)

        if (response.status == false) {
            stop = true;
        };

        console.log("RSPONSE FOR: ", index, " TO ", toIndex, " WAS: ", response.status);

        index = toIndex;

        await new Promise(resolve => setTimeout(resolve, 20000));

    }
    console.log("done toIndex = ", toIndex);
